import { build } from "esbuild";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = path.resolve(import.meta.dirname, "..");
const outputDir = path.join(root, "apps", "next", "public", "artifact-modules");
const moduleDefinitions = [
  {
    id: "demo-stat",
    packageDir: "artifact-ui-demo",
    entry: "src/browser.ts",
    description: "React component for a compact statistic card.",
    exports: ["mount"],
    payloadSchema: "src/generated/demo-stat.schema.json",
  },
  {
    id: "circuit",
    packageDir: "circuit-ui",
    entry: "src/browser.ts",
    description: "Interactive RF/electrical circuit React component and circuit helpers.",
    exports: ["mount", "DEFAULT_CIRCUIT", "normalizeCircuitData"],
    payloadSchema: "src/generated/circuit-data.schema.json",
    worker: "src/routing.worker.ts",
  },
];

await mkdir(outputDir, { recursive: true });
const modules = [];

for (const definition of moduleDefinitions) {
  const packageDir = path.join(root, "packages", definition.packageDir);
  const outputFile = path.join(outputDir, `${definition.id}.js`);
  await build({
    entryPoints: [path.join(packageDir, definition.entry)],
    outfile: outputFile,
    bundle: true,
    format: "esm",
    platform: "browser",
    target: "es2020",
    jsx: "automatic",
  });

  if (definition.worker) {
    const source = await readFile(outputFile, "utf8");
    await writeFile(outputFile, source.replace("./routing.worker.ts", "./circuit-routing-worker.js"));
    await build({
      entryPoints: [path.join(packageDir, definition.worker)],
      outfile: path.join(outputDir, "circuit-routing-worker.js"),
      bundle: true,
      format: "esm",
      platform: "browser",
      target: "es2020",
    });
  }

  modules.push({
    id: definition.id,
    importPath: `/artifact-modules/${definition.id}.js`,
    description: definition.description,
    exports: definition.exports,
    usage: "In iframe JavaScript, dynamically import importUrl, then call module.mount(element, payload). The returned function unmounts the module.",
    payloadSchema: JSON.parse(
      await readFile(path.join(packageDir, definition.payloadSchema), "utf8"),
    ),
  });
}

await writeFile(
  path.join(root, "apps", "uvicorn", "artifact_modules.py"),
  `# Generated by scripts/build-artifact-modules.mjs.\nimport json\n\nARTIFACT_MODULES = json.loads(r'''${JSON.stringify(modules, null, 2)}''')\n`,
);
