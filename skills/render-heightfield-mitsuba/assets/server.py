_last_hf_mtime = 0.0


def check_hf_reload():
    """Check if heightfield.py was modified; reimport if so."""
    global _last_hf_mtime
    try:
        hf_path = Path(heightfield.__file__).resolve()
        mt = hf_path.stat().st_mtime
        if _last_hf_mtime == 0:
            _last_hf_mtime = mt
            return False
        if mt > _last_hf_mtime + 0.1:  # 100ms debounce
            _last_hf_mtime = mt
            return True
    except OSError:
        pass
    return False


def reload_heightfield():
    """Re-import heightfield module after file change."""
    global heightfield
    try:
        heightfield = importlib.reload(heightfield)
        logger.info("heightfield.py reloaded")
    except Exception as e:
        logger.warning("Failed to reload heightfield.py: %s", e)


async def hf_watchdog(interval=2.0):
    """Background task: periodically check for heightfield.py edits."""
    while True:
        await asyncio.sleep(interval)
        if check_hf_reload():
            reload_heightfield()


