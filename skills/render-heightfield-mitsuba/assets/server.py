class MitsubaTrack(VideoStreamTrack):
    kind = "video"

    def __init__(self, renderer):
        super().__init__()
        self._renderer = renderer
        self._counter = 0

    async def recv(self):
        pts, time_base = await self.next_timestamp()
        t0 = time.time()
        frame_rgba = await self._renderer.render_frame()

        video_frame = VideoFrame.from_ndarray(frame_rgba, format="rgba")
        video_frame.pts = pts
        video_frame.time_base = time_base

        dt = (time.time() - t0) * 1000
        self._counter += 1
        if self._counter % 30 == 0:
            logger.info(
                "Frame %d: %dx%d, %.1fms, total_spp=%d",
                self._counter, self._renderer.width, self._renderer.height,
                dt, self._renderer.total_spp,
            )
        return video_frame


