pcs = set()


async def handle_offer(request: web.Request):
    body = await request.json()
    offer = RTCSessionDescription(sdp=body["sdp"], type=body["type"])
    pc = RTCPeerConnection()
    pcs.add(pc)

    renderer = request.app["renderer"]
    data_channel_ref = []

    @pc.on("datachannel")
    def on_datachannel(channel):
        logger.info("Data channel opened: %s", channel.label)
        data_channel_ref.append(channel)
        @channel.on("message")
        def on_message(msg):
            try:
                data = json.loads(msg)
            except Exception:
                return
            if data.get("type") == "camera":
                renderer.update_camera(
                    azimuth=data.get("azimuth", renderer._azimuth),
                    elevation=data.get("elevation", renderer._elevation),
                    distance=data.get("distance", renderer._distance),
                    target=data.get("target"),
                )

    class MitsubaTrackDC(MitsubaTrack):
        async def recv(self):
            frame = await super().recv()
            if data_channel_ref and data_channel_ref[0].readyState == "open":
                msg = json.dumps({"total_spp": self._renderer.total_spp})
                data_channel_ref[0].send(msg)
            return frame

    track = MitsubaTrackDC(renderer)

    @pc.on("connectionstatechange")
    async def on_state():
        if pc.connectionState in ("failed", "closed", "disconnected"):
            pcs.discard(pc)

    pc.addTrack(track)
    await pc.setRemoteDescription(offer)
    answer = await pc.createAnswer()
    await pc.setLocalDescription(answer)

    resp = web.json_response({
        "sdp": pc.localDescription.sdp,
        "type": pc.localDescription.type,
    })
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def handle_camera(request: web.Request):
    body = await request.json()
    renderer = request.app["renderer"]
    renderer.update_camera(
        azimuth=body.get("azimuth", renderer._azimuth),
        elevation=body.get("elevation", renderer._elevation),
        distance=body.get("distance", renderer._distance),
    )
    resp = web.json_response({"ok": True, "total_spp": renderer.total_spp})
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def handle_config(request: web.Request):
    body = await request.json()
    renderer = request.app["renderer"]
    if "width" in body or "height" in body:
        renderer.update_resolution(body.get("width", 640), body.get("height", 480))
    if "spp" in body:
        renderer.spp_per_frame = body["spp"]
    resp = web.json_response({
        "ok": True,
        "width": renderer.width,
        "height": renderer.height,
        "spp_per_frame": renderer.spp_per_frame,
    })
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def get_state(request: web.Request):
    renderer = request.app["renderer"]
    resp = web.json_response({
        "azimuth": renderer._azimuth,
        "elevation": renderer._elevation,
        "distance": renderer._distance,
        "target": renderer._target.tolist(),
        "total_spp": renderer.total_spp,
        "width": renderer.width,
        "height": renderer.height,
    })
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def handle_scene_post(request: web.Request):
    body = await request.json()
    renderer = request.app["renderer"]
    renderer.update_scene(body)
    resp = web.json_response({"ok": True, "scene": renderer.get_scene_spec()})
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def handle_scene_get(request: web.Request):
    renderer = request.app["renderer"]
    resp = web.json_response(renderer.get_scene_spec())
    resp.headers["Access-Control-Allow-Origin"] = "*"
    return resp


async def handle_preview(request: web.Request):
    """GET /preview?period=0.0003&hmax=0.00001&rotation_speed=0.0&center_x=0&center_y=0&size_mm=2.0
    Returns a PNG thumbnail computed by heightfield.heightfield_preview()."""
    q = request.query
    try:
        period = float(q.get("period", 0.0003))
        hmax = float(q.get("hmax", 0.00001))
        rotation_speed = float(q.get("rotation_speed", 0.0))
        center_x = float(q.get("center_x", 0.0))
        center_y = float(q.get("center_y", 0.0))
        size_mm = float(q.get("size_mm", 2.0))
    except (ValueError, TypeError):
        return web.Response(status=400, text="Invalid query parameters")

    png = heightfield.heightfield_preview(
        width_px=128, height_px=128,
        period=period, hmax=hmax, rotation_speed=rotation_speed,
        center_x=center_x, center_y=center_y, size_mm=size_mm,
    )
    resp = web.Response(body=png, content_type="image/png")
    resp.headers["Access-Control-Allow-Origin"] = "*"
    resp.headers["Cache-Control"] = "no-cache"
    return resp


async def handle_options(request: web.Request):
    resp = web.Response()
    resp.headers["Access-Control-Allow-Origin"] = "*"
    resp.headers["Access-Control-Allow-Methods"] = "GET, POST, OPTIONS"
    resp.headers["Access-Control-Allow-Headers"] = "Content-Type"
    return resp


async def handle_index(request: web.Request):
    return web.FileResponse(SITE_DIR / "index.html")


async def on_shutdown(app):
    await asyncio.gather(*(pc.close() for pc in pcs), return_exceptions=True)
    pcs.clear()


def create_app(renderer: MitsubaRenderer):
    app = web.Application()
    app["renderer"] = renderer
    app.router.add_post("/offer", handle_offer)
    app.router.add_post("/camera", handle_camera)
    app.router.add_post("/config", handle_config)
    app.router.add_post("/scene", handle_scene_post)
    app.router.add_get("/state", get_state)
    app.router.add_get("/scene", handle_scene_get)
    app.router.add_get("/preview", handle_preview)
    app.router.add_route("OPTIONS", "/{tail:.*}", handle_options)
    app.router.add_get("/", handle_index)
    app.router.add_static("/", SITE_DIR)
    app.on_shutdown.append(on_shutdown)
    return app


async def main():
    renderer = MitsubaRenderer(width=640, height=480)
    app = create_app(renderer)

    # Start heightfield file watcher
    asyncio.create_task(hf_watchdog())

    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, "127.0.0.1", 8766)
    await site.start()
    logger.info("Server running at http://127.0.0.1:8766")
    await asyncio.Event().wait()


if __name__ == "__main__":
    asyncio.run(main())
