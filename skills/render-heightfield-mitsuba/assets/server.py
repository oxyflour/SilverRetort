"""Mitsuba 3 real-time path tracing server — WebRTC streaming.
Specular heightfield: custom BSDF computes normals from heightfield
differentials at ray-intersection time (1 um step size).
No PLY mesh, no precomputed textures.

Heightfield parameters (period, hmax, rotation_speed, reflectance, envmap path)
are live-configurable via POST /scene — the frontend owns the scene spec.

SPP and camera updates go through a single WebRTC data channel ("stats").
The client (offerer) creates the channel; the server (answerer) receives it
via pc.on("datachannel") and relays both directions.

Heightfield preview: GET /preview?... returns a PNG thumbnail generated by
heightfield.py (pure Python math, same logic as Dr.Jit version below).
heightfield.py is watched for changes and auto-reloaded.
"""

